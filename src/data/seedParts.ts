type PartSeed = Omit<Part, "searchUrl">;

const retailer = "Newegg B2B";

function createSearchUrl(brand: string, model: string) {
  return `https://www.newegg.com/p/pl?d=${encodeURIComponent(`${brand} ${model}`)}`;
}

function createPart(part: PartSeed): Part {
  return {
    ...part,
    searchUrl: createSearchUrl(part.brand, part.model),
  };
}

